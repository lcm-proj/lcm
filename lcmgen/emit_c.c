#define INDENT(n) (4 * (n))

#define emit_start(n, ...)                \
    do {                                  \
        fprintf(f, "%*s", INDENT(n), ""); \
        fprintf(f, __VA_ARGS__);          \
    } while (0)
#define emit_continue(...)       \
    do {                         \
        fprintf(f, __VA_ARGS__); \
    } while (0)
#define emit_end(...)            \
    do {                         \
        fprintf(f, __VA_ARGS__); \
        fprintf(f, "\n");        \
    } while (0)
#define emit(n, ...)                      \
    do {                                  \
        fprintf(f, "%*s", INDENT(n), ""); \
        fprintf(f, __VA_ARGS__);          \
        fprintf(f, "\n");                 \
    } while (0)

#define FLAG_NONE 0

// flags for emit_c_array_loops_start
#define FLAG_EMIT_MALLOCS 1

// flags for emit_c_array_loops_end
#define FLAG_EMIT_FREES 2

static inline int imax(int a, int b)
{
    return (a > b) ? a : b;
}

// Returns a new pointer.
static char *dots_to_underscores(const char *str)
{
    char *p = strdup(str);

    for (char *t = p; *t != 0; t++)
        if (*t == '.')
            *t = '_';

    return p;
}

// Returns a messy pointer.
static char *add_space_or_empty(const char *str)
{
    size_t l = strlen(str);
    if (!l)
        return "";

    char *p = (char *) malloc(l + 2);
    sprintf(p, "%s ", str);

    return p;
}

static void emit_auto_generated_warning(FILE *f)
{
    fprintf(f,
            "// THIS IS AN AUTOMATICALLY GENERATED FILE.  DO NOT MODIFY\n"
            "// BY HAND!!\n"
            "//\n"
            "// Generated by lcm-gen\n\n");
}

// Some types do not have a 1:1 mapping from lcm types to native C
// storage types. Do not free the string pointers returned by this
// function.
// Returns a messy pointer.
static const char *map_type_name(const char *type_name)
{
    if (!strcmp(type_name, "boolean"))
        return "int8_t";

    if (!strcmp(type_name, "string"))
        return "char*";

    if (!strcmp(type_name, "byte"))
        return "uint8_t";

    return dots_to_underscores(type_name);
}

void setup_c_options(getopt_t *gopt)
{
    getopt_add_string(gopt, 0, "c-cpath", ".", "Location for .c files");
    getopt_add_string(gopt, 0, "c-hpath", ".", "Location for .h files");
    getopt_add_string(gopt, 0, "c-export-include", "", "#include that provides the export symbol");
    getopt_add_string(gopt, 0, "c-export-symbol", "", "ABI export decoration symbol");
    getopt_add_string(gopt, 0, "cinclude", "", "Generated #include lines reference this folder");
    getopt_add_bool(gopt, 0, "c-no-pubsub", 0, "Do not generate _publish and _subscribe functions");
    getopt_add_bool(gopt, 0, "c-typeinfo", 0, "Generate typeinfo functions for each type");
}

/** Emit output that is common to every header file **/
static void emit_header_top(lcmgen_t *lcm, FILE *f, char *name)
{
    emit_auto_generated_warning(f);

    fprintf(f, "#ifndef _%s_h\n", name);
    fprintf(f, "#define _%s_h\n", name);
    fprintf(f, "\n");

    fprintf(f, "#include <stdint.h>\n");
    fprintf(f, "#include <stdlib.h>\n");
    if (getopt_get_bool(lcm->gopt, "use-quotes-for-includes"))
        fprintf(f, "#include \"lcm/lcm_coretypes.h\"\n");
    else
        fprintf(f, "#include <lcm/lcm_coretypes.h>\n");

    if (!getopt_get_bool(lcm->gopt, "c-no-pubsub")) {
        if (getopt_get_bool(lcm->gopt, "use-quotes-for-includes"))
            fprintf(f, "#include \"lcm/lcm.h\"\n");
        else
            fprintf(f, "#include <lcm/lcm.h>\n");
    }
    if (strlen(getopt_get_string(lcm->gopt, "c-export-include"))) {
        fprintf(f, "#include \"%s%s%s\"\n", getopt_get_string(lcm->gopt, "cinclude"),
                strlen(getopt_get_string(lcm->gopt, "cinclude")) > 0 ? "/" : "",
                getopt_get_string(lcm->gopt, "c-export-include"));
    }
    fprintf(f, "\n");

    fprintf(f, "#ifdef __cplusplus\n");
    fprintf(f, "extern \"C\" {\n");
    fprintf(f, "#endif\n");
    fprintf(f, "\n");
}

/** Emit output that is common to every header file **/
static void emit_header_bottom(lcmgen_t *lcm, FILE *f)
{
    fprintf(f, "#ifdef __cplusplus\n");
    fprintf(f, "}\n");
    fprintf(f, "#endif\n");
    fprintf(f, "\n");
    fprintf(f, "#endif\n");
}

static void emit_comment(FILE *f, int indent, const char *comment)
{
    if (!comment)
        return;

    gchar **lines = g_strsplit(comment, "\n", 0);
    int num_lines = 0;
    for (num_lines = 0; lines[num_lines]; num_lines++) {
    }

    if (num_lines == 1) {
        emit(indent, "/// %s", lines[0]);
    } else {
        emit(indent, "/**");
        for (int line_ind = 0; lines[line_ind]; line_ind++) {
            if (strlen(lines[line_ind])) {
                emit(indent, " * %s", lines[line_ind]);
            } else {
                emit(indent, " *");
            }
        }
        emit(indent, " */");
    }
    g_strfreev(lines);
}

/** Emit header file output specific to a particular type of struct. **/
static void emit_header_struct(lcmgen_t *lcm, FILE *f, lcm_struct_t *structure)
{
    char *type_name = dots_to_underscores(structure->structname->lctypename);
    char *tn_upper = g_utf8_strup(type_name, -1);

    // include header files required by members
    for (unsigned int i = 0; i < g_ptr_array_size(structure->members); i++) {
        lcm_member_t *member = (lcm_member_t *) g_ptr_array_index(structure->members, i);

        if (!lcm_is_primitive_type(member->type->lctypename) &&
            strcmp(member->type->lctypename, structure->structname->lctypename)) {
            char *other_tn = dots_to_underscores(member->type->lctypename);
            fprintf(f, "#include \"%s%s%s.h\"\n", getopt_get_string(lcm->gopt, "cinclude"),
                    strlen(getopt_get_string(lcm->gopt, "cinclude")) > 0 ? "/" : "", other_tn);
            free(other_tn);
        }
    }

    // output constants
    for (unsigned int i = 0; i < g_ptr_array_size(structure->constants); i++) {
        lcm_constant_t *constant = (lcm_constant_t *) g_ptr_array_index(structure->constants, i);
        assert(lcm_is_legal_const_type(constant->lctypename));
        const char *suffix = "";
        if (!strcmp(constant->lctypename, "int64_t")) {
            suffix = "LL";
        }
        emit_comment(f, 0, constant->comment);
        emit(0, "#define %s_%s %s%s", tn_upper, constant->membername, constant->val_str, suffix);
    }
    if (g_ptr_array_size(structure->constants) > 0) {
        emit(0, "");
    }

    // define the struct
    emit_comment(f, 0, structure->comment);
    emit(0, "typedef struct _%s %s;", type_name, type_name);
    emit(0, "struct _%s", type_name);
    emit(0, "{");

    for (unsigned int m = 0; m < g_ptr_array_size(structure->members); m++) {
        lcm_member_t *member = (lcm_member_t *) g_ptr_array_index(structure->members, m);

        emit_comment(f, 1, member->comment);

        int ndim = g_ptr_array_size(member->dimensions);
        if (ndim == 0) {
            emit(1, "%-10s %s;", map_type_name(member->type->lctypename), member->membername);
        } else {
            if (lcm_is_constant_size_array(member)) {
                emit_start(1, "%-10s %s", map_type_name(member->type->lctypename),
                           member->membername);
                for (unsigned int d = 0; d < ndim; d++) {
                    lcm_dimension_t *ld =
                        (lcm_dimension_t *) g_ptr_array_index(member->dimensions, d);
                    emit_continue("[%s]", ld->size);
                }
                emit_end(";");
            } else {
                emit_start(1, "%-10s ", map_type_name(member->type->lctypename));
                for (unsigned int d = 0; d < ndim; d++)
                    emit_continue("*");
                emit_end("%s;", member->membername);
            }
        }
    }
    emit(0, "};");
    emit(0, "");

    free(type_name);
    g_free(tn_upper);
}

