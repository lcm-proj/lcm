        emit(0, "    lcm_t *lcm, const char *channel, %s_handler_t handler, void *userdata);",
             type_name);
        emit(0, "");
        emit(0, "/**");
        emit(0, " * Removes and destroys a subscription created by %s_subscribe()", type_name);
